package br.com.oitodigital.eletrix.spritekit;

public class SKSize {
	private int width;
	private int height;
	
	public SKSize(int w, int h) {
		width = w;
		height = h;
	}

	public int getWidth() {
		return width;
	}
	
	public void setWidth(int w) {
		width = w;
	}
	
	public int getHeight() {
		return height;
	}
	
	public void setHeight(int h) {
		height = h;
	}
}
